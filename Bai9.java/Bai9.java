package baitapbuoi9;

public class buoi9 {
    public static void tamGiacSaoNguoc(int n)
    {
        System.out.println();
        for(int i=n; i >=1; i--)
        {
            for(int j=1; j<=i; j++)
            {
                System.out.print(j + " ");
            }
            System.out.println();
        }
    }    
    public static void main(String[] args) {
        tamGiacSaoNguoc(7);
    }
}

